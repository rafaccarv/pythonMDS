# Estrutura de repetição for 
# O número colocado no range é a quantidade de vezes a ser repetido
# Por boas praticas de programação usa-se a varíavel i no for, porém pode ser qualquer nome de varíavel 



# for com 1 paramentro 
for i in range(10): # i começa em 0 e vai até range-1, nesse caso do 0 ao 9 (10-1)
    print("senai", i)


# for com 2 parametros
for i in range(10,20):
    print(i)

# for com 3 paramentros 
for i in range(0,10,5):
    print(i)